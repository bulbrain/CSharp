using System;

class FloatDouble
{
    static void Main()
    {
        double first = 34.567839023d;
        float second = 12.345f;
        double third = 8923.1234857d;
        float fourth = 3456.091f;
        Console.WriteLine("1st number is {0}", first);
        Console.WriteLine("2nd number is {0}", second);
        Console.WriteLine("3rd number is {0}", third);
        Console.WriteLine("4th number is {0}", fourth);
    }
}